# Operadores Aritimeticos
print(2 + 2) # Adicao 
print(4 - 2) # subtracao
print(10 / 5) # divisao
print(3 * 2) # multitlicacao
print(3 // 2) # quociente inteiro da divisao
print(3 % 2) # Resto de uma divisao
print(3 ** 2) # potenciacao
print(pow(3, 2)) #potenciacao

print("=====================")

# Operadores de Comparacao
print(3 == 2) # igualdade
print(3 != 2) # Diferente
print(3 > 2)  # Maior que
print(3 < 2)  # Menor que
print(3 >= 2) # Maior ou igual
print(3 <= 2) # Menor ou igual

print("=====================")

# Operadores de Atribicao
x = 2 # Atribuicao simples
x += 2 # equivale a x = x + 2    ADICAO
x -= 2 # equivale a x = x - 2    SUBTRACAO
x *= 2 # equivale a x = x * 2    MULTIPLICACAO
x /= 2 # equivale a x = x / 2    DIVISAO
x %= 2 # equivale a x = x % 2    RESTO INTEIRO DA DIVISAO
x **= 2 # equivale a x = x ** 2  POTENCIA
x //= 2 # equivale a x = x // 2  QUOCIENTE INTEIRO DA DIVISAO

